// ============================================================
// SC 源码再生器 —— AST → 规范化 sc 源码
// ============================================================
// 从 AST 反向生成整齐格式化的 sc 源码。
// 主要用于 VSCode 插件的"树结构源码视图"（--emit-sc 模式）。
//
// 与 codegen_c 的区别：
//   - codegen_c 生成 C 代码（类型映射、语法转换）
//   - codegen_sc 生成 sc 源码（保持原始语法结构，仅做规范化缩进和格式）
//
// 生成的 sc 代码与 --ast JSON 树可对照阅读：
//   左侧 AST 视图看到某个节点，右侧 .tree.sc 看到对应的规范化源码，
//   方便理解 AST 结构和源码的对应关系。
// ============================================================
std::string emitSc(const Program& prog);
