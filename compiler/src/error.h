#pragma once
#include <string>

// ============================================================
// 编译错误结构体
// ============================================================
// 整个编译器使用统一的异常机制报告错误：
//   - 词法分析、语法分析、代码生成各阶段，发现错误时构造 CompileError 并 throw
//   - main.cpp 中的 try/catch 统一捕获所有异常
//   - 格式化为 "文件:行号: 错误: 消息" 输出到 stderr
//   - line=0 表示无法确定行号（极少见，如文件级错误）
// ============================================================
struct CompileError {
    std::string msg;  // 错误描述文本
    int line = 0;      // 出错行号（1-based），默认 0 表示未知行
};
